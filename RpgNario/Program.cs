opcoes.Add(1, new MenuRegistrarEditora());
opcoes.Add(2, new MenuRegistrarSistema());
opcoes.Add(3, new MenuMostrarEditoras());
opcoes.Add(4, new MenuMostrarSistemas());
opcoes.Add(5, new MenuMostrarSistemasPorEngine());
opcoes.Add(-1, new MenuSair());

